        guard let topAsDetailController = secondaryAsNavController.topViewController as? DisplayViewController else { return false }
        if topAsDetailController.choreDisplay == nil {
            // Return true to indicate that we have handled the collapse by doing nothing; the secondary controller will be discarded.
            return true
        }
        return false
    }
    

